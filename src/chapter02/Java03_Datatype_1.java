package chapter02;

public class Java03_Datatype_1 {
    public static void main(String[] args) {
        //todo 基本資料型態
        //todo 1.整數類型
        //byte 8位 -128 ~ 127 (-2的七次方~2的七次方-1 總共是2的八次方個)
        byte b = 1;
        //short 16位  -32768 ~ 32767
        short s = 1;
        //int 32位
        int i = 1;
        //long 64位
        long l = 1;
        // todo 浮點類型(有小數點的)
        //float 單精度 數據要用F(f)結尾
        float f = 1.0f;
        //double 雙精度
        double d = 2.0;
        //todo 字符類型
        //使用符號標示文字內容
        char c = 'c';
        //todo 布林類型
        //只會有 true / false
        boolean bool = true;

    }

}
